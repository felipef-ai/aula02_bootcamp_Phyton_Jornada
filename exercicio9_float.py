# 9 - Faça um programa que converta a temperatura de Celsius para Fahrenheit.

print("Vamos converter a temperatura de Celsius para Fahrenheit, para isso, siga as instruções abaixo.")
celsius = float(input("Por favor, digite a temperatura atual em Graus Celsius: "))
fahrenheit = (celsius * 9/5) + 32
print(f"A temperatura de {celsius}ºC equivale a {fahrenheit}ºF")