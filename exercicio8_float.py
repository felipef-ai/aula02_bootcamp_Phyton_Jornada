#8 Desenvolva um programa que calcule a potência de um número (base e expoente fornecidos pelo usuário).
print("Vamos calcular a potência de um número, siga abaixo a nossa intrução!!!")
base = float(input("Digite um número base: "))
expoente = float(input("Digite um número expoente: "))
potencia = base ** expoente
print("A potência é: ", potencia)