#7 Crie um programa que calcule a média de dois números flutuantes fornecidos pelo usuário.

num_1 = float(input("Digite um número flutuante: "))
num_2 = float(input("Digite um outro número flutuante: "))
calculo_media = (num_1 + num_2) / 2
print("Esse é o calculo da média dos números digitados: ", calculo_media)
